    private void bindVehicleTable(){
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("GreenLogisticsAuditorPU");
        VehicleJpaController vjc = new VehicleJpaController(emf);
        List<Vehicle> vehicleList = vjc.findVehicleEntities();
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"VehicleID","VehicleType","FuelType","FuelConsumption","DistanceTravelled"});
        for(Vehicle vehicle:vehicleList){
            model.addRow(new String[]{vehicle.getVid().toString(),vehicle.getVehicletype(),vehicle.getFueltype(),vehicle.getFuelconsumption().toString(),vehicle.getKilometers().toString()});
            
        }
        jTable1.setModel(model);
    }
    
    private void bindEmissionTable(){
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("GreenLogisticsAuditorPU");
        EmissionsJpaController ejc = new EmissionsJpaController(emf);
        List<Emissions> emissionList = ejc.findEmissionsEntities();
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"VehicleType","CO2","CO","VOC","NOX","SOX","PM10"});
        for(Emissions emissions:emissionList){
            model.addRow(new String[]{emissions.getVehicletype(),emissions.getCo2().toString(),emissions.getCo().toString(),
            emissions.getVoc().toString(),emissions.getNox().toString(),emissions.getSox().toString(),emissions.getPm10().toString()});
        }
        jTable4.setModel(model);
    }
    
