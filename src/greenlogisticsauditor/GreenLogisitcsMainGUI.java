    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (selectedVehicleID != -1) {
            try {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("GreenLogisticsAuditorPU");
            VehicleJpaController vjc = new VehicleJpaController(emf);
            Vehicle vehicle = vjc.findVehicle(selectedVehicleID);
            vehicle.setVid(Integer.parseInt(jTextField1.getText()));
            vehicle.setFueltype(jComboBox1.getSelectedItem().toString());
            vehicle.setVehicletype(jComboBox2.getSelectedItem().toString());
            vehicle.setFuelconsumption(Double.parseDouble(jTextField2.getText()));
            vehicle.setKilometers(Double.parseDouble(jTextField3.getText()));
            vjc.edit(vehicle);
        } catch (Exception ex) {
            Logger.getLogger(GreenLogisitcsMainGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        selectedVehicleID = -1;
        clearVehicleTextField();
        bindVehicleTable();
        }else{
            JOptionPane.showMessageDialog(null, "You must select a row first!");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

