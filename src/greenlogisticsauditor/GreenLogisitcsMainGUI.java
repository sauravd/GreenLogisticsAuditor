            /*********************************
            This is the emission calculation:
            *********************************/            
            //double fuelConsmp = Double.parseDouble(jTextField2.getText());
            double fuelConsmp = ve.getFuelconsumption();
            //double kmtravel = Double.parseDouble(jTextField3.getText());
            double kmtravel = ve.getKilometers();
            
